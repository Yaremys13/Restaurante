package rest.model;

import java.math.BigDecimal;

public class Restaurante {

	
	private Long idrestaurante;
	private String nomrestaurante;
	private String dirrestaurante;
	private BigDecimal latrestaurante;
	private BigDecimal lngrestaurante;
	private String emailrestaurante;
	private String twitterrestaurante;
	private String fbrestaurante;
	private String instrestaurante;
	private String estatus;
	
	
	public Long getIdrestaurante() {
		return idrestaurante;
	}
	public void setIdrestaurante(Long idrestaurante) {
		this.idrestaurante = idrestaurante;
	}
	public String getNomrestaurante() {
		return nomrestaurante;
	}
	public void setNomrestaurante(String nomrestaurante) {
		this.nomrestaurante = nomrestaurante;
	}
	public String getDirrestaurante() {
		return dirrestaurante;
	}
	public void setDirrestaurante(String dirrestaurante) {
		this.dirrestaurante = dirrestaurante;
	}
	public BigDecimal getLatrestaurante() {
		return latrestaurante;
	}
	public void setLatrestaurante(BigDecimal latrestaurante) {
		this.latrestaurante = latrestaurante;
	}
	public BigDecimal getLngrestaurante() {
		return lngrestaurante;
	}
	public void setLngrestaurante(BigDecimal lngrestaurante) {
		this.lngrestaurante = lngrestaurante;
	}
	public String getEmailrestaurante() {
		return emailrestaurante;
	}
	public void setEmailrestaurante(String emailrestaurante) {
		this.emailrestaurante = emailrestaurante;
	}
	public String getTwitterrestaurante() {
		return twitterrestaurante;
	}
	public void setTwitterrestaurante(String twitterrestaurante) {
		this.twitterrestaurante = twitterrestaurante;
	}
	public String getFbrestaurante() {
		return fbrestaurante;
	}
	public void setFbrestaurante(String fbrestaurante) {
		this.fbrestaurante = fbrestaurante;
	}
	public String getInstrestaurante() {
		return instrestaurante;
	}
	public void setInstrestaurante(String instrestaurante) {
		this.instrestaurante = instrestaurante;
	}
	public String getEstatus() {
		return estatus;
	}
	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}
	
	

	
}
